const startOffset = ref(100) // Initial offset value
const body = ref(null)

// Add and clean up scroll listener
onMounted(() => {
    const body = document.getElementById('body')

    // Update startOffset based on scroll
    function updateStartOffset() {
        const scrollTop = window.scrollY || body.scrollTop
        const scrollHeight = body.scrollHeight - window.innerHeight

        // Calculate scroll percentage
        const scrollPercent = (scrollTop / scrollHeight) * 100

        // Set startOffset based on scroll percentage
        startOffset.value = 100 - scrollPercent * 2 // Adjust direction as needed
    }
    body.addEventListener('scroll', updateStartOffset)
})

onUnmounted(() => {
    body.value?.removeEventListener('scroll')
})
